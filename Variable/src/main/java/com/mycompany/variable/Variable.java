public class Variable {

    public static void main(String[] args) {
       //Declaración de variables
       
       //Tipo de dato entero:
       int numero = 5;
       
       //Tipo de dato cadena de carácteres:
       /*
       Cuando declaramos cadenas de caráteres 
       usamos String empezando con mayúsculas
       y en el valor de la cadena usamos comillas dobles
       */
       String nombre = "Dante";
        
       //Tipo de dato carácter (Uno solo)
       /*
       Cuando solo es un solo carácter usamos char y en el valor
       del carácter usamos comillas simples ''
       */
       char letra = 'a';
       
       //Tipo de dato decimal usamos double
       double temperatura = 38.5;
       
       //Tipo de dato Booleano 
       /*
       usamos Boolean, en el valor sera
       true o false
       */
       boolean SiONo= true;
       
       //Tipo de dato largo
       /*
       Este dato es un entero con varios números
       y usamos long para declarar
       */
       long largo = 123456789;
      
       
        
    } // Fin de mi método
    
} //Fin de mi clase 
